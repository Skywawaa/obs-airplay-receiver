/* Phase 2: response body is 32 bytes starting with these 20 fixed bytes,
 * followed by 12 bytes derived from the client's encrypted data. */
static const uint8_t fp_header[] = {
	0x46, 0x50, 0x4c, 0x59, 0x03, 0x01, 0x04, 0x00,
	0x00, 0x00, 0x00, 0x14
};

struct fairplay *fairplay_create(void)
{
	return calloc(1, sizeof(struct fairplay));
}

void fairplay_destroy(struct fairplay *fp)
{
	free(fp);
}

uint8_t *fairplay_handle_setup(struct fairplay *fp, const uint8_t *req,
			       size_t req_len, size_t *out_len)
{
	if (!fp || !req || !out_len)
		return NULL;

	*out_len = 0;

	/* Request starts with "FPLY" header */
	if (req_len < 16 || req[0] != 'F' || req[1] != 'P' ||
	    req[2] != 'L' || req[3] != 'Y') {
		ap_warn("fp-setup: invalid request header");
		return NULL;
	}

	/* Type byte at offset 6 determines phase */
	uint8_t phase = req[6];

	if (phase == 0x01 && req_len == 16) {
		/* Phase 1: client sends 16 bytes, mode is at byte[14] */
		uint8_t mode = req[14];
		if (mode > 3) {
			ap_warn("fp-setup phase 1: unknown mode %d", mode);
			return NULL;
		}

		fp->last_mode = mode;

		uint8_t *reply = malloc(142);
		if (!reply)
			return NULL;

		memcpy(reply, fp_reply1[mode], 142);
		*out_len = 142;
		ap_info("fp-setup phase 1 OK (mode %d)", mode);
		return reply;

	} else if (phase == 0x03 && req_len == 164) {
		/* Phase 2: client sends 164 bytes
		 * Response: 12-byte fixed header + 20 bytes derived from
		 * the request. We echo bytes from the client's encrypted
		 * section - specifically bytes 144..163 of the request. */
		uint8_t *reply = malloc(32);
		if (!reply)
			return NULL;

		memcpy(reply, fp_header, 12);
		memcpy(reply + 12, req + 144, 20);
		*out_len = 32;
		ap_info("fp-setup phase 2 OK");
		return reply;
	}

	ap_warn("fp-setup: unexpected request (phase=%u, len=%zu)",
		phase, req_len);
	return NULL;
}
